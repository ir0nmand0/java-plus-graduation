package ru.yandex.practicum.model;

/**
 * Перечисление типов действий пользователя с событиями.
 * Используется для классификации взаимодействий пользователей с системой.
 */
public enum ActionType {
    /**
     * Просмотр события пользователем
     */
    VIEW,

    /**
     * Регистрация пользователя на событие
     */
    REGISTER,

    /**
     * Отметка "Нравится" для события от пользователя
     */
    LIKE
}